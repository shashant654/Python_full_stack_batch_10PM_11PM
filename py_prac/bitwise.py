## Bitwise Operations in Python:-
# => Bitwise operations are used to perform operations on binary numbers.
# => it works on bits and performs bit by bit operation.

# EX: &, |, ^, ~, 

# AND(&) Operator:-
# It returns 1 if both the bits are 1.

a = 10
b = 4
x = 5
y = 3
print(a & b)  # 0
print(x & y) # 1

print( 5 & 6)



