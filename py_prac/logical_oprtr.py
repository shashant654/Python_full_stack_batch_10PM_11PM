## Logical Operators:-
# => It is used to combine two or more conditions.
# => it returns boolean value(True or False)


# EX: and, or, not 

# in C and other programming languages:
# and => &&           
# or => ||
# not => !



# AND Operator:-
# It returns True if both the conditions are True.
# otherwise, it returns False.

# OR Operator:-
# It returns True if at least one condition is True.
# otherwise, it returns False.

A = 10
B = 20
print("the output of this A < B and A == 10 is:", A < B and A == 10)
print("the output of this A > B and A != B is:", A > B and A != B)

print("the output of this A < B or A == 10 is:", A < B or A == 10)

print(A < B or A > 50)
